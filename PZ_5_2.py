"Дан прямоугольник, длины сторон которого равны натуральным числам A и B. Составить функцию, которая будет находить, на сколько квадратов можно разрезать данный прямоугольник, если от него каждый раз отрезать квадрат наибольшей площади."

def count_squares(a, b):
    count = 0
    while a > 0 and b > 0:
        if a >= b:
            count += a // b
            a = a % b
        else:
            count += b // a
            b = b % a
    return count

try:
    A = int(input("Введите длину стороны A: "))
    B = int(input("Введите длину стороны B: "))
    
    if A <= 0 or B <= 0:
        print("Ошибка: стороны должны быть натуральными числами (больше нуля).")
    else:
        result = count_squares(A, B)
        print(f"Прямоугольник {A}x{B} можно разрезать на {result} квадратов.")
        
except ValueError:
    print("Ошибка: введите целые числа.")
except Exception as e:
    print(f"Неожиданная ошибка: {e}")